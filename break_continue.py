#break
# 1 to 10 print
for i in range(1,11):         # it will print from 1 to 10
    if i == 5:
        break                 # when the value of i = 5, the loop will stop
    print(i)
    
    
    
#continue
for i in range(1,11):         #it will print from 1 to 10
    if i == 5: 
        continue              #it will skip "5" while printing and print (1,2,3,4,6,7,8,9,10)
    print(i) 
    
